    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // SEARCH LOGIC HERE:
        try {
            connect c = new connect();

            String sql = "select dr_id,name,age,phone_number,fees from doctor where dr_id=?";
            PreparedStatement p = c.c.prepareStatement(sql);
            p.setString(1, id.getText());
            ResultSet rs = p.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "RECORD FOUND");
                name.setText(rs.getString("NAME"));
                age.setText(rs.getString("AGE"));

                phonenumber.setText(rs.getString("PHONE_NUMBER"));
                fEES.setText(rs.getString("fees"));
                //logic so that the data on screen can be altered cant be changed by anyone
                id.setEditable(false);
                name.setEditable(false);
fEES.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(null, "RECORD NOT FOUND");
                name.setText("");
                id.setText("");
                age.setText("");

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // DELETE LOGIC HERE
        if (this.isAgeValid() && this.isNameValid() && this.isPhoneNoValid()) {
            try {
                // get selected  fees 
                String fees = (String) this.jComboBox1.getSelectedItem();

                connect c = new connect();
                //logic for inserting the data to be deleted in another table

                String sql = "delete from doctor where dr_ID = ?";

                PreparedStatement p = c.c.prepareStatement(sql);
                p.setString(1, id.getText());

                p.executeUpdate();

                JOptionPane.showMessageDialog(null, "RECORD SUCCESSFULLY DELETED");
                id.setText("");
                name.setText("");
                age.setText("");
                phonenumber.setText("");
                fEES.setText("");

            } catch (Exception ee) {
                JOptionPane.showMessageDialog(null, ee);

            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed

        if (this.isAgeValid() && this.isNameValid() && this.isPhoneNoValid()) {

            // UPDATE LOGIC HERE
            try {
                // get the selected Fees
                String fees = (String) this.jComboBox1.getSelectedItem();

                connect c = new connect();
                
                String sql1 = "update doctor set  AGE = ?,PHONE_NUMBER=?,fees=? where dr_ID = ?";

//prepared statement for sql1
                PreparedStatement ps = c.c.prepareStatement(sql1);

                ps.setString(1, age.getText());
                ps.setString(2, phonenumber.getText());
                ps.setString(3, fees);
                ps.setString(4, id.getText());


                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "RECORD UPDATED SUCCESSFULLY");
                name.setText("");
                id.setText("");
                age.setText("");
                phonenumber.setText("");
                  fEES.setText("");
                id.setEditable(true);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_BtnUpdateActionPerformed

