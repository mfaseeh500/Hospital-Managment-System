    public boolean isAgeValid() {
        String REGEX_EMAIL = "[\\d]{2}";
        boolean matcher = Pattern.matches(REGEX_EMAIL, this.age.getText());

        this.age_validity.setVisible(!matcher);

        return matcher;
    }

    public boolean isPhoneNoValid() {
        String REGEX_Phone = "\\+[1-9]{1}[0-9]{3,14}";
        boolean matcher = Pattern.matches(REGEX_Phone, this.phonenumber.getText());

        this.phno_validity.setVisible(!matcher);

        return matcher;
    }

    public boolean isNameValid() {
        String REGEX_NAME = "[a-zA-Z]{1}[a-zA-Z0-9]{3,20}";
        boolean matcher = Pattern.matches(REGEX_NAME, this.name.getText());

        this.name_validity.setVisible(!matcher);

        return matcher;
    }

